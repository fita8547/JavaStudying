package javastudclass;

public class TypeCasting2 {
    public static void main(String[] args) {
        long value5 = 10L;
        long value6 = 10l;
        float value7 = 5.8F;
        float value8 = 5.8f;

        System.out.printf(String.valueOf(value5));
        System.out.printf(String.valueOf(value6));
        System.out.printf(String.valueOf(value7));
        System.out.printf(String.valueOf(value8));
    }
}
